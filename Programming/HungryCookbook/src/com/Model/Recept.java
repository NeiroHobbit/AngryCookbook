package com.Model;

public class Recept {

	private String idRecept = "";
	private String nameRecept = "";
	private String textRecept = "";
	private String typeRecept = "";

	public String getIdRecept() {
		return idRecept;
	}

	public void setIdRecept(String idRecept) {
		this.idRecept = idRecept;
	}

	public String getNameRecept() {
		return nameRecept;
	}

	public void setNameRecept(String nameRecept) {
		this.nameRecept = nameRecept;
	}

	public String getTextRecept() {
		return textRecept;
	}

	public void setTextRecept(String textRecept) {
		this.textRecept = textRecept;
	}

	public String getTypeRecept() {
		return typeRecept;
	}

	public void setTypeRecept(String typeRecept) {
		this.typeRecept = typeRecept;
	}

}
